    <h2> Coki Pardede{{$id}} </h2> 
    <p> Kasat Narkoba Polres Metro Tangerang Kota AKBP Pratomo Widodo mengatakan Coki ditangkap di perumahan di daerah Pagedangan, Kota Tangerang.
Ketika ditangkap, Coki disebut masih dalam pengaruh sabu. Selain Coki, dalam kasus ini, polisi juga menangkap seorang perempuan berinisial WLI yang merupakan seorang penyuplai sabu.</p>
    <p> Dari hasil tes urine, keduanya dinyatakan positif ampetamin dan metampetamin.
Penangkapan Coki merupakan hasil pengembangan dari informasi masyarakat. Dari informasi itu, dilakukan penyelidikan hingga ditemukan seorang kurir yang kemudian mengarah kepada Coki.</p>
