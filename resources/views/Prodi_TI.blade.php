    <h1><center> Teknik Informatika </center></h1>
    <p><center>  Teknik Informatika merupakan kumpulan disiplin ilmu dan teknik
         yang secara khusus menangani masalah transformasi atau pengolahan data 
         dengan memanfaatkan se optimal mungkin teknologi komputer melalui proses proses logika. 
         Pada teknik informatika bidang ilmu yang lebih banyak dikaji adalah bidang pemrograman dan komputasi, 
         rekayasa perangkat lunak (software) untuk berbagai bidang aplikasi dalam berbagai bidang usaha, dan teknologi jaringan komputer.
